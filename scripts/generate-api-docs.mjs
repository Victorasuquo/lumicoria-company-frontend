import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { resolve } from 'node:path'

const defaultSource = 'http://127.0.0.1:8000/openapi/client.json'
const source = process.argv[2] || process.env.PORTAL_OPENAPI_SOURCE || defaultSource
const docsDirectory = resolve('docs/generated')
const endpointOutput = resolve(docsDirectory, 'CLIENT_PORTAL_ENDPOINT_REFERENCE.md')
const schemaOutput = resolve(docsDirectory, 'CLIENT_PORTAL_SCHEMA_REFERENCE.md')
const methodOrder = ['get', 'post', 'put', 'patch', 'delete']

async function loadContract(value) {
  if (/^https?:\/\//i.test(value)) {
    const response = await fetch(value)
    if (!response.ok) {
      throw new Error(`Unable to load ${value}: HTTP ${response.status}`)
    }
    return response.json()
  }

  return JSON.parse(await readFile(resolve(value), 'utf8'))
}

function escapeCell(value) {
  return String(value ?? '')
    .replaceAll('|', '\\|')
    .replaceAll('\n', '<br>')
}

function schemaName(reference) {
  return reference?.split('/').at(-1) ?? reference
}

function schemaType(schema = {}) {
  if (schema.$ref) {
    return `\`components["schemas"]["${schemaName(schema.$ref)}"]\``
  }
  if (schema.const !== undefined) {
    return `literal \`${JSON.stringify(schema.const)}\``
  }
  if (schema.enum) {
    return schema.enum.map((value) => `\`${JSON.stringify(value)}\``).join(' or ')
  }
  if (schema.anyOf) {
    return schema.anyOf.map(schemaType).join(' or ')
  }
  if (schema.oneOf) {
    return schema.oneOf.map(schemaType).join(' or ')
  }
  if (schema.allOf) {
    return schema.allOf.map(schemaType).join(' and ')
  }
  if (schema.type === 'array') {
    return `array of ${schemaType(schema.items ?? {})}`
  }
  if (Array.isArray(schema.type)) {
    return schema.type.map((value) => `\`${value}\``).join(' or ')
  }
  if (schema.type === 'object' || schema.properties || schema.additionalProperties) {
    if (schema.additionalProperties && typeof schema.additionalProperties === 'object') {
      return `record of ${schemaType(schema.additionalProperties)}`
    }
    return '`object`'
  }
  if (schema.type) {
    return `\`${schema.type}${schema.format ? `:${schema.format}` : ''}\``
  }
  return '`unknown`'
}

function schemaRules(schema = {}) {
  const rules = []
  if (schema.default !== undefined) rules.push(`default ${JSON.stringify(schema.default)}`)
  if (schema.example !== undefined) rules.push(`example ${JSON.stringify(schema.example)}`)
  if (schema.minimum !== undefined) rules.push(`minimum ${schema.minimum}`)
  if (schema.maximum !== undefined) rules.push(`maximum ${schema.maximum}`)
  if (schema.minLength !== undefined) rules.push(`minimum length ${schema.minLength}`)
  if (schema.maxLength !== undefined) rules.push(`maximum length ${schema.maxLength}`)
  if (schema.minItems !== undefined) rules.push(`minimum items ${schema.minItems}`)
  if (schema.maxItems !== undefined) rules.push(`maximum items ${schema.maxItems}`)
  if (schema.pattern) rules.push(`pattern \`${schema.pattern}\``)
  if (schema.readOnly) rules.push('read-only')
  if (schema.writeOnly) rules.push('write-only')
  if (schema.nullable) rules.push('nullable')
  return rules.join('; ')
}

function contentSchemas(content = {}) {
  const entries = Object.entries(content)
  if (!entries.length) return 'None'
  return entries
    .map(([contentType, media]) => `${contentType}: ${schemaType(media.schema ?? {})}`)
    .join('<br>')
}

function securityLabel(operation) {
  if (operation.security?.length === 0) return 'Public'
  if (operation.security?.length) {
    return operation.security
      .flatMap((requirement) => Object.keys(requirement))
      .map((name) => `\`${name}\``)
      .join(', ')
  }
  return operation['x-access'] ? `\`${operation['x-access']}\`` : 'Portal session or declared route policy'
}

function operationParameters(pathItem, operation) {
  return [...(pathItem.parameters ?? []), ...(operation.parameters ?? [])]
}

function operationEntries(contract) {
  const entries = []
  for (const [path, pathItem] of Object.entries(contract.paths ?? {})) {
    for (const method of methodOrder) {
      const operation = pathItem[method]
      if (!operation) continue
      entries.push({ method, operation, path, pathItem })
    }
  }
  return entries
}

function endpointReference(contract) {
  const operations = operationEntries(contract)
  const groups = new Map()

  for (const entry of operations) {
    const tag = entry.operation.tags?.[0] ?? 'untagged'
    const group = groups.get(tag) ?? []
    group.push(entry)
    groups.set(tag, group)
  }

  const lines = [
    '# Lumicoria Client Portal Endpoint Reference',
    '',
    '> Generated from the client-safe OpenAPI contract. Do not edit this file manually.',
    '',
    `- Contract: **${contract.info?.title ?? 'Lumicoria Client Portal API'}**`,
    `- Contract version: **${contract.info?.version ?? 'unversioned'}**`,
    `- Client-safe operations: **${operations.length}**`,
    `- Type source: \`src/api/generated/schema.ts\``,
    `- Regenerate: \`npm run api:sync\``,
    '',
    '## Contract Rules',
    '',
    '- Use only `/openapi/client.json` for browser code generation.',
    '- Prefix portal-relative resource paths with `/api/v1/client-portal`.',
    '- Send `credentials: "include"` for the HTTP-only portal session.',
    '- Send `X-Organization-ID` on tenant-scoped requests.',
    '- Reuse one `Idempotency-Key` while retrying the same user intent.',
    '- Preserve and resend `ETag` values through `If-Match` for guarded writes.',
    '- Parse `application/problem+json` and retain `request_id` for support.',
    '',
    '## Operation Counts',
    '',
    '| Tag | Operations |',
    '|---|---:|',
    ...[...groups.entries()]
      .sort(([left], [right]) => left.localeCompare(right))
      .map(([tag, entries]) => `| \`${escapeCell(tag)}\` | ${entries.length} |`),
    `| **Total** | **${operations.length}** |`,
    '',
  ]

  let sequence = 0
  for (const [tag, entries] of [...groups.entries()].sort(([left], [right]) => left.localeCompare(right))) {
    lines.push(`## ${tag}`, '')

    for (const { method, operation, path, pathItem } of entries) {
      sequence += 1
      const parameters = operationParameters(pathItem, operation)
      const permissions = operation['x-permissions'] ?? []
      const access = operation['x-access'] ?? 'Declared by route security and permissions'
      const requestBody = operation.requestBody
      const responses = Object.entries(operation.responses ?? {})

      lines.push(
        `### ${sequence}. \`${method.toUpperCase()} ${path}\``,
        '',
        operation.summary ?? 'No summary provided.',
        '',
        '| Field | Value |',
        '|---|---|',
        `| Operation ID | \`${escapeCell(operation.operationId ?? 'not-declared')}\` |`,
        `| Generated Type | \`operations["${escapeCell(operation.operationId ?? 'not-declared')}"]\` |`,
        `| Access | ${escapeCell(access)} |`,
        `| Security | ${securityLabel(operation)} |`,
        `| Permissions | ${permissions.length ? permissions.map((permission) => `\`${escapeCell(permission)}\``).join(', ') : 'None declared'} |`,
        `| Code Owner | ${operation['x-code-owner'] ? `\`${escapeCell(operation['x-code-owner'])}\`` : 'Not declared'} |`,
        `| Mutation Controls | ${parameters.filter((parameter) => ['Idempotency-Key', 'If-Match'].includes(parameter.name)).map((parameter) => `\`${parameter.name}\``).join(', ') || 'None declared'} |`,
        '',
        '#### Parameters',
        '',
      )

      if (parameters.length) {
        lines.push(
          '| Name | Location | Required | Type | Description / Rules |',
          '|---|---|---:|---|---|',
          ...parameters.map((parameter) => {
            const details = [parameter.description, schemaRules(parameter.schema)].filter(Boolean).join('; ')
            return `| \`${escapeCell(parameter.name)}\` | ${escapeCell(parameter.in)} | ${parameter.required ? 'Yes' : 'No'} | ${schemaType(parameter.schema)} | ${escapeCell(details || 'None declared')} |`
          }),
        )
      } else {
        lines.push('No parameters.')
      }

      lines.push('', '#### Request Body', '')
      if (requestBody) {
        lines.push(
          `- Required: **${requestBody.required ? 'yes' : 'no'}**`,
          `- Content: ${contentSchemas(requestBody.content)}`,
        )
        if (requestBody.description) lines.push(`- Description: ${requestBody.description}`)
      } else {
        lines.push('No request body.')
      }

      lines.push(
        '',
        '#### Responses',
        '',
        '| Status | Description | Content |',
        '|---:|---|---|',
        ...responses.map(([status, response]) => (
          `| \`${escapeCell(status)}\` | ${escapeCell(response.description ?? 'No description')} | ${contentSchemas(response.content)} |`
        )),
        '',
      )
    }
  }

  return `${lines.join('\n').trim()}\n`
}

function schemaReference(contract) {
  const schemas = Object.entries(contract.components?.schemas ?? {})
    .sort(([left], [right]) => left.localeCompare(right))
  const lines = [
    '# Lumicoria Client Portal Schema Reference',
    '',
    '> Generated from the client-safe OpenAPI contract. Do not edit this file manually.',
    '',
    `- Contract version: **${contract.info?.version ?? 'unversioned'}**`,
    `- Component schemas: **${schemas.length}**`,
    '- Type namespace: `components["schemas"]`',
    '- Regenerate: `npm run api:sync`',
    '',
    '## Using Generated Schemas',
    '',
    '```ts',
    "import type { components } from '../../src/api/generated/schema'",
    '',
    "type PortalSession = components['schemas']['PortalSessionResponse']",
    '```',
    '',
  ]

  for (const [name, schema] of schemas) {
    const properties = Object.entries(schema.properties ?? {})
    const required = new Set(schema.required ?? [])

    lines.push(
      `## \`${name}\``,
      '',
      `- Generated Type: \`components["schemas"]["${name}"]\``,
      `- Shape: ${schemaType(schema)}`,
    )
    if (schema.description) lines.push(`- Description: ${schema.description}`)
    if (schema.enum) {
      lines.push(`- Allowed values: ${schema.enum.map((value) => `\`${JSON.stringify(value)}\``).join(', ')}`)
    }
    if (schema.allOf) {
      lines.push(`- Composition: ${schema.allOf.map(schemaType).join(' and ')}`)
    }
    if (schema.anyOf) {
      lines.push(`- Alternatives: ${schema.anyOf.map(schemaType).join(' or ')}`)
    }

    lines.push('')
    if (properties.length) {
      lines.push(
        '| Property | Type | Required | Description / Rules |',
        '|---|---|---:|---|',
        ...properties.map(([propertyName, propertySchema]) => {
          const details = [propertySchema.description, schemaRules(propertySchema)].filter(Boolean).join('; ')
          return `| \`${escapeCell(propertyName)}\` | ${schemaType(propertySchema)} | ${required.has(propertyName) ? 'Yes' : 'No'} | ${escapeCell(details || 'None declared')} |`
        }),
        '',
      )
    } else {
      lines.push('This schema does not declare object properties.', '')
    }
  }

  return `${lines.join('\n').trim()}\n`
}

const contract = await loadContract(source)
await mkdir(docsDirectory, { recursive: true })
await Promise.all([
  writeFile(endpointOutput, endpointReference(contract)),
  writeFile(schemaOutput, schemaReference(contract)),
])

const operations = operationEntries(contract).length
const schemas = Object.keys(contract.components?.schemas ?? {}).length
process.stdout.write(`Generated ${operations} operations and ${schemas} schemas.\n`)
